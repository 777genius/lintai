#[test]
fn heuristic_rules_live_in_preview_and_structural_rules_stay_stable() {
    for spec in rule_specs() {
        match spec.detection_class {
            DetectionClass::Heuristic => {
                assert_eq!(
                    spec.metadata.tier,
                    RuleTier::Preview,
                    "heuristic rule {} must stay preview",
                    spec.metadata.code
                );
            }
            DetectionClass::Structural => {
                if matches!(
                    spec.metadata.code,
                    "SEC313"
                        | "SEC323"
                        | "SEC325"
                        | "SEC328"
                        | "SEC336"
                        | "SEC401"
                        | "SEC402"
                        | "SEC403"
                ) {
                    assert_eq!(
                        spec.metadata.tier,
                        RuleTier::Preview,
                        "structural preview rule {} must stay preview",
                        spec.metadata.code
                    );
                } else {
                    assert_eq!(
                        spec.metadata.tier,
                        RuleTier::Stable,
                        "structural rule {} must stay stable",
                        spec.metadata.code
                    );
                }
            }
        }
    }
}

